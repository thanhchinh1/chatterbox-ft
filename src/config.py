from dataclasses import dataclass

@dataclass
class TrainConfig:
    # --- Paths ---
    # Directory where setup.py downloaded the files
    model_dir: str = "./pretrained_models"
    
    # Path to your metadata CSV (Format: ID|RawText|NormText)
    csv_path: str = "./MyTTSDataset/metadata.csv"
    metadata_path: str = "./metadata.json"
    
    # Directory containing WAV files
    wav_dir: str = "./MyTTSDataset/wavs"
    #wav_dir: str = "./FileBasedDataset"
    
    preprocessed_dir = "./MyTTSDataset/preprocess"
    #preprocessed_dir = "./FileBasedDataset/preprocess"
    
    # Output directory for the finetuned model
    output_dir: str = "./chatterbox_output"
    resume_from_checkpoint: str = "./chatterbox_output/checkpoint-14000"
    
    is_inference = False
    inference_prompt_path: str = "./speaker_reference/chinh.wav"
    inference_test_text: str = "Xin chào, đây là bản thử giọng tiếng Việt sau khi fine-tune."


    # Dataset format is fixed to LJSpeech in this fork.
    ljspeech = True
    json_format = False
    preprocess = False # If you've already done preprocessing once, set it to false.
    
    # Vietnamese-only fork keeps Standard mode by default.
    # Turbo carries a very large multilingual BPE vocabulary.
    is_turbo: bool = False # Set True if you're training Turbo, False if you're training Normal.
    vietnamese_only: bool = True

    # --- Runtime ---
    force_single_gpu: bool = True
    single_gpu_id: int = 0

    # --- Vocabulary ---
    # The size of the NEW vocabulary (from tokenizer.json)
    # Ensure this matches the JSON file generated by your tokenizer script.
    # For Turbo mode: Use the exact number provided by setup.py (e.g., 52260)
    new_vocab_size: int = 52260 if is_turbo else 201

    # --- Hyperparameters ---
    batch_size: int = 8        # Adjust based on VRAM (2, 4, 8)
    grad_accum: int = 2        # Effective Batch Size = Batch * Accum
    learning_rate: float = 1e-5 # T3 is sensitive, keep low
    num_epochs: int = 500
    
    save_steps: int = 5000
    save_total_limit: int = 2
    dataloader_num_workers: int = 8

    # --- Constraints ---
    start_text_token = 255
    stop_text_token = 0
    max_text_len: int = 256
    max_speech_len: int = 650   # Truncates very long audio
    prompt_duration: float = 3.0 # Duration for the reference prompt (seconds)
